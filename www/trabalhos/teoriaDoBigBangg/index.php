        <h1>A Teoria do Big Bang</h1>
            <h2>Atualizações Explosivas</h2>      
            <p>
                Assim como o Universo, a Física e a Astronomia estão sempre em expansão.
                Desejamos demonstrar com este projeto a Teoria do Big Bang e todos os estudos científicos que comprovam que este foi o início do Universo em que vivemos. Porém, também mostraremos novas descobertas que contestam certas informações, como a quanto tempo o Big Bang ocorreu, ou até mesmo a própria teoria. Com isso desejamos chegar a uma conclusão se a teoria realmente é incontestável, assim como as informações que possuímos hoje, mas principalmente, demonstrar que ainda há muito a se descobrir na ciência e no universo.
                O principal objetivo do projeto além da demonstração dos fatos que tornam a Teoria do big Bang a mais aceita cientificamente, é despertar mais interesse pela ciência e a curiosidade de descobrir mais sobre o Universo. Desejamos com este demonstrar o quanto falta se descobrir e comprovar, e que muitas coisas na Ciência ainda são contestáveis, portanto, precisamos sempre de mais pesquisa e mais estudos. Iremos buscar artigos e estudos científicos, e com eles fazer uma espécie de “Debate Teórico” entre as comprovações e contestações da Teoria do Big Bang e suas informações.</p>

