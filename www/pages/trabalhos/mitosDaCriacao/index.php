        <h1>Mitos da Criação</h1>
        <p>
            Através de muitas dúvidas por de trás das histórias da criação do universo, pensamos em realizar um projeto sobre os mitos de criação. Para que possamos aprimorar nosso conhecimento sobre o universo em que fazemos parte, com os mitos e crenças podemos explicar às pessoas sobre o seu lugar no mundo em que se encontram, fortificando assim sua fé. 
            O projeto abordará as histórias que tentam explicar a formação do Universo. Os mitos da criação satisfazem muitas dúvidas importantes, “como surgimos?” e “como o universo foi criado?” Estes mitos se diferem bastante de uma etnia para outra, algumas dessas etnias possuem muitos mitos de criação ou até mesmo variações de um mesmo mito, fazendo com que seja mais interessante o contato com outras histórias e etnias como as que entrevistamos, pesquisamos e iremos apresentar a seguir.
        </p>

