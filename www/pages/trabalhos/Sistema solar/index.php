        <h1>PLANETAS DO SISTEMA SOLAR</h1>      
            <p>
                Através do interesse em comum dentre as integrantes do grupo, notamos a
                necessidade de mostrar o quão interessante é o universo, sendo assim, este projeto
                consiste em investigar a formação de sistemas planetários, em especial do Sistema
                Solar, sua estrutura e características dos principais corpos celestes que o compõe.
                Dentre as fontes de pesquisa para o desenvolvimento do trabalho, utilizamos
                teorias cientistas, como a teoria aperfeiçoada por Immanuel Kant, que defendia a
                teoria da Nebulosa Solar - uma nuvem de gás e poeira do cosmos que está
                relacionada com a origem do Sistema Solar - o qual é composto por oito planetas e
                diversos outros astros do Universo, como asteróides, satélites…
